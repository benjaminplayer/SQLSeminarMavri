    public void fillMenus(){
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        try {
            Connection con = OracleConnection.getConnection();
            Statement stm = con.createStatement();
            ResultSet rs = null;
            con.setAutoCommit(false);
            
            rs = stm.executeQuery("select IDKUPEC, ime, PRIIMEK from KUPEC\n" +
                "order by IDKUPEC asc");
            con.commit();
            while(rs.next()){
                jComboBox1.addItem(rs.getInt(1)+","+rs.getString(2)+" "+rs.getString(3));
            }
            
            
            rs = stm.executeQuery("select idspored, ime, DATUMPREDVAJANJA from SPORED\n" +
                "inner join film using(idfilm)\n" +
                "order by IDSPORED asc");
            con.commit();
            while(rs.next()){
                jComboBox2.addItem(rs.getInt(1)+","+rs.getString(2)+"; Date: "+rs.getDate(3).toString());
            }
            
            rs.close();
            stm.close();
            con.close();
            
            
        } catch (SQLException ex) {
            Logger.getLogger(VstopnicaInsert.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
        public int getID(){
        int id = 0;
        try {
            Connection con= OracleConnection.getConnection();
            Statement stm = null;
            ResultSet rs = null;
            stm = con.createStatement();
            con.setAutoCommit(false);
            
            rs = stm.executeQuery("select max(idVstopnica) from Vstopnica");
            con.commit();
            while(rs.next())
               id = rs.getInt(1);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        return id+1;
    }
    

