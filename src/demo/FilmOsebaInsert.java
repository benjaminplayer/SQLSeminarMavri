    private void fillMenus() {
        try {
            jComboBox1.removeAllItems();
            jComboBox2.removeAllItems();
            jComboBox3.removeAllItems();
            Connection con = OracleConnection.getConnection();
            Statement stm = null;
            ResultSet rs = null;
            con.setAutoCommit(false);
            stm = con.createStatement();
            rs = stm.executeQuery("select IDFILM, ime from FILM\n"
                    + "order by idfilm");
            con.commit();

            while (rs.next()) {
                jComboBox1.addItem(rs.getInt(1) + "," + rs.getString(2));
            }

            rs = stm.executeQuery("select IDOSEBA, ime, priimek from oseba\n"
                    + "order by IDOSEBA");

            while (rs.next()) {
                jComboBox2.addItem(rs.getInt(1) + "," + rs.getString(2) + " " + rs.getString(3));
            }

            rs = stm.executeQuery("select distinct vloga from FILM_OSEBA\n"
                    + "order by vloga asc");

            while (rs.next()) {
                jComboBox3.addItem(rs.getString(1));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

