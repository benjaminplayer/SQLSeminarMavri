    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jToggleButton1.isSelected()) {
            if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "The name must not be empty!");
                return;
            }
            try {
                Connection con = OracleConnection.getConnection();
                Statement stm = null;
                con.setAutoCommit(false);
                ResultSet rs = null;
                stm = con.createStatement();

                rs = stm.executeQuery("select distinct vloga from FILM_OSEBA\n"
                        + "order by vloga asc");
                con.commit();
                while (rs.next()) {
                    if (rs.getString(1).equals(jTextField1.getText())) {
                        JOptionPane.showMessageDialog(this, "The role with that name already exists!");
                        return;
                    }

                }
                rs.close();
                
                
                stm.execute("insert into FILM_OSEBA(IDFILM, IDOSEBA, VLOGA)\n"
                        + "VALUES(" + Integer.parseInt(jComboBox1.getSelectedItem().toString().substring(0, jComboBox1.getSelectedItem().toString().indexOf(","))) + ","
                        + Integer.parseInt(jComboBox2.getSelectedItem().toString().substring(0, jComboBox2.getSelectedItem().toString().indexOf(","))) + ",'"
                        +jTextField1.getText().replace("'", "''")+"')");
                con.commit();

                stm.close();
                con.close();
                
                                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                jComboBox3.setSelectedItem(0);
                jTextField1.setText(null);
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            fillMenus();
            jTextField1.setText(null);
        } else {
            try {
                Connection con = OracleConnection.getConnection();
                Statement stm = null;
                con.setAutoCommit(false);
                stm = con.createStatement();

                stm.execute("insert into FILM_OSEBA(IDFILM, IDOSEBA, VLOGA)\n"
                        + "VALUES(" + Integer.parseInt(jComboBox1.getSelectedItem().toString().substring(0, jComboBox1.getSelectedItem().toString().indexOf(","))) + ","
                        + Integer.parseInt(jComboBox2.getSelectedItem().toString().substring(0, jComboBox2.getSelectedItem().toString().indexOf(","))) + ",'"
                        + jComboBox3.getSelectedItem().toString() + "')");
                con.commit();
                stm.close();
                con.close();
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                jComboBox3.setSelectedItem(0);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

